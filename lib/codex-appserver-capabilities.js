import { execFileSync } from 'node:child_process';
import { mkdtempSync, readFileSync, rmSync } from 'node:fs';
import { tmpdir } from 'node:os';
import { join } from 'node:path';

import { CODEX_REQUEST_USER_INPUT_METHODS } from './codex-request-user-input.js';

const cache = new Map();

export function extractRequestUserInputMethods(schema) {
  const text = typeof schema === 'string' ? schema : JSON.stringify(schema || {});
  return CODEX_REQUEST_USER_INPUT_METHODS.filter(method => text.includes(`"${method}"`));
}

function invocation(codexPath, args) {
  return codexPath.endsWith('.js')
    ? { command: process.execPath, args: [codexPath, ...args] }
    : { command: codexPath, args };
}

/** Verify support against the schema generated by the exact CLI binary CXV launches. */
export function inspectCodexRequestUserInputSupport(codexPath) {
  if (!codexPath) return { verified: false, version: null, methods: [], error: 'missing Codex path' };
  if (cache.has(codexPath)) return cache.get(codexPath);

  const outDir = mkdtempSync(join(tmpdir(), 'cxv-codex-schema-'));
  let result;
  try {
    const versionCall = invocation(codexPath, ['--version']);
    const version = execFileSync(versionCall.command, versionCall.args, {
      encoding: 'utf8',
      timeout: 10000,
      stdio: ['ignore', 'pipe', 'pipe'],
    }).trim();
    const schemaCall = invocation(codexPath, [
      'app-server',
      'generate-json-schema',
      '--experimental',
      '--out',
      outDir,
    ]);
    execFileSync(schemaCall.command, schemaCall.args, {
      encoding: 'utf8',
      timeout: 15000,
      stdio: ['ignore', 'pipe', 'pipe'],
    });
    const schema = readFileSync(join(outDir, 'ServerRequest.json'), 'utf8');
    const methods = extractRequestUserInputMethods(schema);
    result = {
      verified: methods.length > 0,
      version,
      methods,
      error: methods.length > 0 ? null : 'request_user_input server request absent from generated schema',
    };
  } catch (error) {
    result = {
      verified: false,
      version: null,
      methods: [],
      error: error?.message || String(error),
    };
  } finally {
    try { rmSync(outDir, { recursive: true, force: true }); } catch {}
  }
  cache.set(codexPath, result);
  return result;
}

export function _resetCodexCapabilityCacheForTests() {
  cache.clear();
}
